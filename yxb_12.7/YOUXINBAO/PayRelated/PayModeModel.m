#import "PayModeModel.h"

@implementation PayModeModel

-(id)initWithImageName:(NSString *)imageName title:(NSString *)title detail:(NSString *)detail payMode:(Pay_Mode)payMode
{
    self = [super init];
    if (self) {
        _logoImageName = imageName;
        _logoTitle = title;
        _logoDetail = detail;
        _payMode = payMode;
    }
    
    return self;
}

@end
